    public void soloLetras(char c, KeyEvent evt, JTextField txt) {
        if (!Character.isLetter(c) && !Character.isISOControl(c) && !Character.isSpace(c)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(txt, "Solo letras");
        }
    }
    
    public void soloNumeros(char c, KeyEvent evt, JTextField txt) {
        if (((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE) && (c != '.') && !Character.isISOControl(c)) {
            JOptionPane.showMessageDialog(txt, "Solo puede ingresar números");
            evt.consume();
        }
        if (c == '.' && txt.getText().contains(".")) {
            evt.consume();
        }
    }
    
