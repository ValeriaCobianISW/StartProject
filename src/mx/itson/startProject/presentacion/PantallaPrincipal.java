    private void txtRfcSocioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRfcSocioKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isISOControl(c) && !Character.isLetterOrDigit(c)) {
            getToolkit().beep();
            evt.consume();
        }
        if (Character.isLowerCase(c)) {
            String cad = ("" + c).toUpperCase();
            c = cad.charAt(0);
            evt.setKeyChar(c);
        }
        if (txtRfcSocio.getText().length() == 12) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_txtRfcSocioKeyTyped

