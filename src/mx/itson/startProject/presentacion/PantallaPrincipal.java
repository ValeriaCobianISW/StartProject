    public void calcularDescuento() {
        DecimalFormat df = new DecimalFormat("00.00");
        double cantidad = Double.parseDouble(txtCantidadPrestamo.getText());
        double interes = Double.parseDouble(txtInteresPrestamo.getText());
        int plazo = cmbPlazoPrestamo.getSelectedIndex();
        double total = cantidad + (cantidad * (interes / 100));
        double descuento = total / (plazo * 52);
        lbTotal.setText(String.valueOf(df.format(total)));
        lbDescuentoPrestamo.setText(String.valueOf(df.format(descuento)));
    }
    
