    public static void cargarRutas() {
        model = (DefaultTableModel) tbRutas.getModel();
        model.getDataVector().clear();
        RutaNegocio rn = new RutaNegocio();
        List<Ruta> rutas = new ArrayList<>();
        rutas = rn.verRutas();
        for (Ruta r : rutas) {
            if (!r.isEstado()) {
                model.addRow(new Object[]{
                    r.getId(),
                    r.getNombre(),
                    r.getDescripcion(),
                    r.getKilometraje()
                });
            }
        }

    }

