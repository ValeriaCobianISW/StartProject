    private void btnSeleccionarRutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarRutaActionPerformed
        int fila = tbRutas.getSelectedRow();
        model = (DefaultTableModel) tbRutas.getModel();
        if (fila != -1) {
            ruta.setId((int) model.getValueAt(fila, 0));
            ruta.setNombre((String) model.getValueAt(fila, 1));
            ruta.setDescripcion((String) model.getValueAt(fila, 2));
            ruta.setKilometraje((int) model.getValueAt(fila, 3));
            ruta.setEstado(true);
            RutaPersistencia rp = new RutaPersistencia();
            rp.editarRuta(ruta);
            modelo = (DefaultTableModel) frmPeriodo.tbRutas.getModel();
            modelo.addRow((new Object[]{
                ruta.getId(),
                ruta.getNombre()
            }));
            this.dispose();
        }
    }//GEN-LAST:event_btnSeleccionarRutaActionPerformed

    public void buscarRuta() throws SQLException {
        model = (DefaultTableModel) tbRutas.getModel();
        model.getDataVector().clear();
        List<Ruta> rutas = new ArrayList<>();
        RutaNegocio rn = new RutaNegocio();
        rutas = rn.verRutas();
        for (Ruta r : rutas) {
            if (r.getNombre().toLowerCase().matches(".*" + txtBuscarRuta.getText().toLowerCase() + ".*")) {
                model.addRow(new Object[]{
                    r.getId(),
                    r.getNombre(),
                    r.getDescripcion(),
                    r.getKilometraje()
                });
            } else {
                tbRutas.setVisible(false);
            }
        }
        tbRutas.setVisible(true);
    }
    private void txtBuscarRutaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarRutaKeyReleased
        try {
            buscarRuta();
        } catch (SQLException ex) {
            Logger.getLogger(frmRutas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtBuscarRutaKeyReleased

