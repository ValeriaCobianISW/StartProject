    public void buscarRfc() {
        model.getDataVector().clear();
        List<Socio> socios = new ArrayList<>();
        SocioPersistencia sp = new SocioPersistencia();
        socios = sp.obtenerTodos();
        for (Socio s : socios) {
            if (s.getRfc().toLowerCase().startsWith(txtCriterioBusqueda.getText().toLowerCase())) {
                model.addRow(new Object[]{
                    s.getId(), s.getRfc(), s.getNombre()
                });
            } else {
                tbSocios.setVisible(false);
            }
        }
        tbSocios.setVisible(true);
    }

    public void buscarNombre() {
        model = (DefaultTableModel) tbSocios.getModel();
        model.getDataVector().clear();
        List<Socio> socios = new ArrayList<>();
        SocioPersistencia sp = new SocioPersistencia();
        socios = sp.obtenerTodos();
        for (Socio s : socios) {
            if (s.getNombre().toLowerCase().matches(".*" + txtCriterioBusqueda.getText().toLowerCase() + ".*")) {
                model.addRow(new Object[]{
                    s.getId(), s.getNombre(), s.getRfc()
                });
            } else {
                tbSocios.setVisible(false);
            }
        }
        tbSocios.setVisible(true);
    }

