    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        model = (DefaultTableModel) tbSocios.getModel();
        int fila = tbSocios.getSelectedRow();
        if (fila != -1) {
            socio = new Socio();
            socio.setId((int) model.getValueAt(fila, 0));
            socio.setRfc((String) model.getValueAt(fila, 1));
            socio.setNombre((String) model.getValueAt(fila, 2));
            if (PantallaPrincipal.indicador == 0) {
                Prestamo p = new Prestamo();
                PrestamoPersistencia pp = new PrestamoPersistencia();
                socio.setEstado(false);
                p = pp.obtenerPorSocio(socio.getId());
                if (p != null) {
                    JOptionPane.showMessageDialog(this, "Este socio ya tiene un pr√©stamo activo");
                    socio = new Socio();
                } else {
                    PantallaPrincipal.lbSocio.setText(socio.getNombre());
                    PantallaPrincipal.indicador = -1;
                    this.dispose();
                }

            } else {
                socio.setEstado(true);
                DecimalFormat df = new DecimalFormat("00.00");
                frmPeriodo.lbSocio.setText(socio.getNombre());
                SocioPersistencia sp = new SocioPersistencia();
                sp.editarSocio(socio);
                PrestamoPersistencia pp = new PrestamoPersistencia();
                frmPeriodo.evaluarPrestamo(socio.getId());
                PantallaPrincipal.indicador = -1;
                this.dispose();
            }

        } else {
            JOptionPane.showMessageDialog(this, "No ha seleccionado ning√∫n socio");
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

